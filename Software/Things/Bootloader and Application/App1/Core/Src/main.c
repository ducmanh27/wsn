void SensingTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
		//HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
		if (flag_update_time == 1)
		{
			MX_RTC_Setup();
			flag_update_time = 0;
		}
		float cel = SHT2x_GetTemperature(1);
		temp_from_LM35 = SHT2x_GetInteger(cel);
//		HAL_I2C_Mem_Read_IT(&hi2c1, DS3231_ADDRESS, 0X00,I2C_MEMADD_SIZE_8BIT, u8_tranBuffer, 7);
		HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
		HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
		real_time = unix_time_in_seconds(&sTime, &sDate);
		if (temp_from_LM35 < temp_threshold)
		{
			//Turn on blue led
			
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_0, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_15, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, GPIO_PIN_RESET);
		}
		else if (temp_from_LM35 >= temp_threshold && temp_from_LM35 <= (temp_threshold + 20))
		{
			//Turn on red led
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_0, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_15, GPIO_PIN_RESET);
		}
		else
		{
			//Turn on green led
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_15, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_0, GPIO_PIN_RESET);
		}
		vTaskDelay(pdMS_TO_TICKS(50));
		//response_print("Temperature from sensor: %d, Warning temperature: %d, Unix Timestamp: %d\n", temp_from_LM35,temp_threshold, real_time);
		//response_print("Datetime: %d:%d:%d %d/%d/%d \n", sTime.Hours, sTime.Minutes, sTime.Seconds, sDate.Date, sDate.Month, sDate.Year);
		send_data(node_id, real_time, temp_from_LM35, x_axis, y_axis, room_id);
		

		
		
		vTaskDelay( pdMS_TO_TICKS( TIME_SENDING ) );
    //osDelay(1);
  }
  /* USER CODE END 5 */
}
